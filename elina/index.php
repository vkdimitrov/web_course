					<strong>Учебен център „Елина” е основан през 1991 година</strong> в гр. София от г-жа Албена Велкова, магистър по социология. Първоначално се предлага обучение на <strong>кандидат-студенти</strong> по български език и литература, история и биология. От следващата година се организират и <strong>кандидатстудентски курсове</strong> по математика, география и химия. Две години по-късно (<strong>1993 г.</strong>) спектърът на предлаганите образователни услуги се допълва с <strong>езикови курсове и частни уроци</strong> по английски, TOEFL, SAT, френски, немски, италиански, испански, руски, португалски и гръцки език за <strong>деца и възрастни</strong>. През <strong>1999 г.</strong> се създаде направлението за <strong>подготовка за кандидатстване след седми клас</strong> като организираме двугодишни (от <a href="#">6 клас</a>) и едногодишни курсове. От <strong>2001 г.</strong> фирмата предлага и <strong>индивидуално обучение</strong> за ученици от <strong>втори</strong> до <strong>дванадесети</strong> клас по <strong>всички</strong> предмети и езици, изучавани в училище. От <strong>2007 г.</strong> подготвяме зрелостници за <strong>матури</strong> по български език и литература, чужд език, математика, философия, история и цивилизация, география и икономика, физика и астрономия, химия и опазване на околната среда, биология и здравно образование.</p>
