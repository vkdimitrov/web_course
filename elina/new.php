<p style="text-align: justify;"><b>&nbsp;
&nbsp;Обучението на кандидат-студенти</b> в <span style="font-weight: bold;">учебен
център
„Елина”</span> е строго <b>съобразено с критериите на
изпита</b>
на съответното висше учебно заведение. Основна задача на подготовката е
изграждане на <b>знания и умения, адекватни на изпитния формат</b>.
Важен акцент в нашите <b>кандидатстудентски
курсове</b> е създаването на траен интерес на ученика към
изучавания предмет, постепенно формиране на самостоятелно и
системно познание.</p>

<p style="text-align: justify;">&nbsp;
&nbsp;Нашата методика предполага: </p>

<ul style="text-align: justify;">

  <li><b>Курсове</b> за подготовка <span style="background-color: rgb(204, 255, 255);"> в малки групи
    </span><b style="background-color: rgb(204, 255, 255);">(до
6 човека)</b> или <b>индивидуално</b>,
което прави възможен диалога по време на занятията и позволява на
учениците да участват като равноправни субекти в процеса на обучение.</li>

  <li>Внимателно и системно контролиране и насочване на
самоподготовката, отчитайки <b>конкретните особености</b>
на всеки ученик.</li>

  <li>Текуща <b>обратна връзка</b> за постигнатия
напредък чрез постоянни задачи за <b>домашна работа, </b>писмени
и устни<b> изпитвания</b>.</li>

  <li>Многократно <b style="font-weight: bold;">трениране</b><span style="font-weight: bold;"> на
кандидат-студентите</span> в <b>реална изпитна ситуация</b>,
с което целим не само проверка и затвърждаване на усвоените знания, но
и изграждане на практически навици, съобразени с изискванията на
жанровия модел (интерпретативно съчинение, тест, други) и
времето за изпълнение на задачата.</li>

  <li><b>Месечна атестация</b> за постиженията и
проблемите на учениците в усвояването на учебния материал.</li>

</ul>

<p style="text-align: justify;">&nbsp; &nbsp;В
нашия учебен център особено се гордеем с <b>авторските ни
помощни материали</b>, разработени от екипа преподаватели. Те са
елемент от цялостната ни система на обучение и улесняват <b>структурирането
и алгоритмизирането на знанията</b>.</p>

<p style="text-align: justify;">&nbsp; &nbsp;За
ученици в <b>11 клас</b> организираме двугодишни курсове
за кандидат-студенти.</p>

<p style="text-align: justify;">&nbsp; &nbsp;За
ученици в <b>12 клас</b> предлагаме: </p>

<p style="text-align: justify;">&nbsp; &nbsp;-<b>
едногодишни
кандидатстудентски курсове</b>
по български език и литература, математика,
история, география, биология и английски език (начало - октомври и
ноември - 2014
г.);</p>

<p style="text-align: justify;">&nbsp; &nbsp;- <strong>&nbsp;интензивна
кандидатстудентска подготовка</strong> от месец февруари -
2015 г.;</p>

<p style="text-align: justify;">&nbsp; &nbsp;- <span style="font-weight: bold;">&nbsp;комбинирани
курсове</span> за подготовка за матура
и кандидатстудентски изпити.</p>

<p style="text-align: justify;">&nbsp; &nbsp;По
всички учебни предмети и езици се предлага също и <span style="font-weight: bold;">индивидуална форма на обучение
(частни
уроци)</span>.</p>

<p style="text-align: justify;">&nbsp;<span style="font-weight: bold;"> </span>Ние, школа
„Елина” и учениците ни, можем да се похвалим с <span style="color: rgb(204, 0, 0);"><span style="font-weight: bold;">единствената пълна шестица</span>
от приемните изпити по <span style="font-weight: bold;">БЕЛ</span>
в Софийски университет „Св. Климент Охридски” <span style="font-weight: bold;">от всички</span>
кандидатстващи през 2009 г.</span>; с оценка <span style="font-weight: bold;">6.00</span> от изпита по <span style="font-weight: bold;">История на България в СУ</span>;
със среден успех на <span style="font-weight: bold;">матурата
по БЕЛ</span> - <span style="font-weight: bold;">5,72</span>
и с <span style="font-weight: bold;">10 пълни шестици</span>,
както и с много <span style="font-weight: bold;">отлични
оценки</span> на изпитите в <span style="font-weight: bold;">УНСС,
ТУ,&nbsp;УАСГ, СУ</span> и др. &nbsp;</p>

<p style="text-align: center;"><img style="width: 312px; height: 177px;" alt="Частни уроци и курсове за подготовка на кандидат-студенти, София." src="img/kandidat_studenti.jpg"></p>

<p style="text-align: justify;"><b>&nbsp;Високата
успеваемост</b> на учениците от <span style="font-weight: bold;">нашите курсове</span> на <b>кандидатстудентските
изпити</b>
в най-престижните <span style="font-weight: bold;">български</span>
и <span style="font-weight: bold;">чуждестранни</span>
университети гарантират, че можете с
основание
да се доверите на дългогодишния ни опит, отлична методика и <b>квалифицирани
преподаватели</b>.</p>

